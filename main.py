def print_greetings():
    print("Hello, welcome to my project!")
    print("Hope you have a great day!")

def print_math_results():
    num1 = 5
    num2 = 10
    result = num1 + num2
    print(f"The result of adding {num1} and {num2} is {result}")

def main():
    print_greetings()
    print("\n---\n")
    print_math_results()

if __name__ == "__main__":
    main()
